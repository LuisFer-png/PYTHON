import matplotlib.pyplot as plt
import numpy as np
from matplotlib.colors import to_rgb

width, height = 32, 32
canvas = np.ones((height, width, 3))

def pintar_pixel(x,y, color):

    try:
        rgb = to_rgb(color)
        canvas[y,x] = rgb
    except ValueError:
        print(f"Color {color} no reconocido")

def pintar_línea(x1,y1,x2,y2, color):
    rgb = to_rgb(color)
    if x1 == x2:
        for y in range(min(y1,y2), max(y1,y2)+1):
            canvas[y, x1] = rgb
    elif y1 == y2:
        for x in range(min(x1,x2), max(x1,x2)+1):
            canvas[y1, x] = rgb
    else:
        print("Solo se permiten líneas horizontales o verticales")

def pintar_rectangulo(x1,y1,x2,y2, color):
    rgb = to_rgb(color)
    for y in range(min(y1,y2), max(y1,y2)+1):
        for x in range(min(x1,x2), max(x1,x2)+1):
            canvas[y,x] = rgb

